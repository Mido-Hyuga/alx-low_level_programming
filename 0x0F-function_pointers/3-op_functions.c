#include "3-calc.h"

/**
* op_add - sum of a and b.
* @a: 1st number.
* @b: 2nd number.
* Return: sum.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - difference of a and b.
* @a: 1st number.
* @b: 2nd number.
* Return: difference.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - product of a and b.
* @a: 1st number.
* @b: 2nd number.
* Return: product.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - division of a and b.
* @a: 1st number.
* @b: 2nd number.
* Return: division.
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of division of a and b.
 * @a: 1st number.
 * @b: 2nd number.
 * Return: mod.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
